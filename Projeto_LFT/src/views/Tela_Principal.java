    public void Executar() throws FileNotFoundException, IOException {
        String texto = txtCodigo.getText();

        if (texto.length() == 0) {
        } else {
            salvar(texto);
            String documento = "yytt.txt";

            try {

                Reader input = documento.length() > 0
                        ? new FileReader(documento)
                        : new InputStreamReader(System.in);

                Lexico_Projeto scanner = new Lexico_Projeto(input);
                Token token;

                ((DefaultTableModel) jTable1.getModel()).setNumRows(0);
                do {

                    token = scanner.yylex();
                    token.type.getValor();
                    DefaultTableModel val = (DefaultTableModel) jTable1.getModel();
                    String lexema;
                    if (token.val == null) {
                        lexema = "--";
                    } else {
                        lexema = token.val.toString();
                    }
                    String linha = Integer.toString(token.col);
                    String coluna = Integer.toString(token.line);
                    Token.T tipo = token.type;
                    if (token.type.getValor() == ERROR) {
                        erro = true;
                    }
                    val.addRow(new String[]{tipo.toString(), coluna, linha, lexema});

                } while (!scanner.isFini());
                input.close();
                deletar();
                JOptionPane.showMessageDialog(null, "Analise Lexica concluida!");

            } catch (IOException e) {
                System.err.println(e);

            }
            deletar();
        }

    }

    public void salvar(String texto) throws IOException {
        File f = new File("yytt.txt");
        FileWriter fw = new FileWriter(f);

        fw.write(texto);
        fw.close();

    }

    public void deletar() {
        File f = new File("yytt.txt");
        f.delete();

    }

